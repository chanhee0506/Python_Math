'''
    첫번째 정수를 입력하세요 :
    두번째 정수를 입력하세요 :
    300 + 400 = 700
    300 - 400 = -100
    300 * 400 = 120000
    300 / 400 = 0.75
'''
x = int(input("첫번째 정수를 입력하세요 :"))
y = int(input("두번째 정수를 입력하세요 :"))

add = x+y
div = x-y
time = x*y
nanu = x/y

print(x,"+",y,"=",add)
print(x,"-",y,"=",div)
print(x,"*",y,"=",time)
print(x,"/",y,"=",nanu)

